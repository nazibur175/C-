#include<bits/stdc++.h>
using namespace std;
int main(){
    long long int a,b;
    cin>>a>>b;
    long long int sum=a+b;
    long long int mul=a*b;
    long long int sub=a-b;

    cout<<a<<" + "<<b<<" = "<<sum<<endl;
    cout<<a<<" * "<<b<<" = "<<mul<<endl;
    cout<<a<<" - "<<b<<" = "<<sub<<endl;


    return 0;
} 

